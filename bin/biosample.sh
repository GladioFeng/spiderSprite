outfile=biosamples.txt
# ----------> parament capture start <------------
if [ "$#" -eq 0 ] ; then echo -e "\ntype $0 -h for help message"
    exit 1
fi
if [ "$#" -gt 0 ]
    then
    for (( i=1; i<="$#"; i++ )) ; do
        case ${!i} in
            -i)
                ((i+=1))
                inp=$(readlink -e ${!i})
                ;;
            -o)
                ((i+=1))
                if [ ! -e ${!i} ] ; then touch ${!i} ; fi
                outfile=$(readlink -e ${!i})
                ;;
            -h)	
                echo "
Help messeage:
    bash $0 -h for help 
        options: 
        -i: <file> -- input file; output file generated by web_ncbi
        -o: [file] -- outputfile; one biosample per line, default is biosamples.txt
        " 
                exit 1
                ;;
            *)
                echo "\"${!i}\" is not a valid parameter. See -h"
                exit 1
                ;;
        esac
    done
fi
# ----------> parament capture end<------------


cat $inp | grep -P "PRJNA\d*" -o | sort | uniq > $outfile
